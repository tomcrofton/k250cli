package org.alleypress.k250.serial;

import java.util.HashMap;

public class K250Commands {
	public static final RawPacket GET_CONFIG = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x10, 0x30, 0x00, 0x06, 0x00, 0x1c});
	
	public static final RawPacket GET_MIDI = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x0e, 0x02, 0x00, 0x00, 0x10, 0x10});
	public static final RawPacket SET_MIDI = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x0f, 0x02, 0x00, 0x00, 0x11});
	public static final RawPacket GET_CONTROLS = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x18, 0x02, 0x00, 0x00, 0x1a});
	public static final RawPacket SET_CONTROLS = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x19, 0x02, 0x00, 0x00, 0x1b});

	public static final RawPacket GET_KBDLIB = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x02, 0x02, 0x00, 0x00, 0x04});
	public static final RawPacket GET_INSTLIB = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x04, 0x02, 0x00, 0x00, 0x06});
	public static final RawPacket GET_SEQLIB = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x00, 0x02, 0x00, 0x00, 0x02});
	public static final RawPacket GET_KEYPADBINS = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x0A, 0x02, 0x00, 0x00, 0x02});
	public static final RawPacket GET_LIST = new RawPacket(new byte[] {0x10, 0x02, 0x00, 0x04, 0x00, 0x0C, 0x02, 0x00, 0x00, 0x0E});

	private static HashMap<String,RawPacket> PACKET_MAP_GET = new HashMap<>();
	static {
		PACKET_MAP_GET.put("config", GET_CONFIG);
	}
	
	public static String getGetCommandList() {
		return PACKET_MAP_GET.keySet().toString();
	}
}
